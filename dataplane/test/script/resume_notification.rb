#!/usr/bin/ruby

require "socket"

udp = UDPSocket.open()
gtpc_addr = Socket.pack_sockaddr_in(2123, "127.0.0.1")

gtpc_resume_notification_req = [
  0x48,0xa4,0x00,0x18,
  0x77,0x88,0x99,0xaa,
  0x03,0xc0,0x08,0x00,
  0x49,0x00,0x01,0x00,0x05,
  0x01,0x00,0x07,0x00,0x71,0x65,0x57,0x74,0x98,0x87,0x10

].pack("C*")

r = udp.send(gtpc_resume_notification_req, 0, gtpc_addr)
print "gtpc_resume_notification_req: #{r}\n"

udp.close
